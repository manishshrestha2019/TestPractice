package model;

public class Arithmetic {
    public float add(float num1,float num2){
        return num1+num2;
    }
    public float subtract(float num1,float num2){
        return num1-num2;
    }
    public float multiply(float num1,float num2){
        return num1*num2;
    }
    public float devide(float num1,float num2){
        return num1/num2;
    }
}
